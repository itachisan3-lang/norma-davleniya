export default async function ArticlePage({ params }) {
  const { slug } = await params;
  const a = ARTICLES.find(x => x.slug === slug);
  if (!a) notFound();

  const content = ARTICLE_CONTENT[slug];
  const schema = {
    '@context': 'https://schema.org',
    '@type': 'Article',
    headline: a.title,
    description: a.description,
    datePublished: a.date,
    publisher: { '@type': 'Organization', name: 'НормаДавления', url: BASE_URL },
    url: `${BASE_URL}/stati/${slug}`,
  };

  return (
    <div className="page-wrap">
      <script type="application/ld+json" dangerouslySetInnerHTML={{ __html: JSON.stringify(schema) }} />
      <div className="container">
        <Breadcrumb items={[{ label: 'Статьи', href: '/stati/kak-pravilno-izmeryat-davlenie' }, { label: a.title }]} />
        <article>
          <section className="page-hero">
            <h1>{a.title}</h1>
            <p className="lead">{a.description}</p>
            <div style={{fontSize:'0.8rem',color:'var(--text-dim)',marginTop:8}}>
              Обновлено: {new Date(a.date).toLocaleDateString('ru-RU', {year:'numeric',month:'long',day:'numeric'})}
            </div>
          </section>

          {content ? (
            content.h2s.map((section, i) => (
              <div key={i} className="card">
                <h2 style={{marginTop:0}}>{section.title}</h2>
                <p>{section.text}</p>
              </div>
            ))
          ) : (
            <div className="card">
              <p>Статья готовится. Скоро здесь появится подробный материал о <strong>{a.title.toLowerCase()}</strong>.</p>
            </div>
          )}

          {content?.faq && (
            <div style={{marginTop:20}}>
              {/* FaqBlock is client — use inline rendering for articles */}
              <h2>Часто задаваемые вопросы</h2>
              {content.faq.map((item, i) => (
                <div key={i} className="info-block" style={{marginBottom:12}}>
                  <h3 style={{marginTop:0}}>{item.q}</h3>
                  <p style={{marginBottom:0,color:'var(--text-muted)'}}>{item.a}</p>
                </div>
              ))}
            </div>
          )}
        </article>
      </div>
    </div>
  );
}
